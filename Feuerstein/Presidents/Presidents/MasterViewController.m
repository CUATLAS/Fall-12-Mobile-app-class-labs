- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    // Assign the string for key "url" to the new VC
    NSDictionary *presidentChosen = [presidents objectAtIndex:indexPath.row];
    NSString *url = [presidentChosen objectForKey:@"url"];
    DetailViewController *detailVC = self.detailViewController;
    detailVC.detailItem = url;
    
    // Set title to president selected
    detailVC.titleString = [presidentChosen objectForKey:@"name"];
    [detailVC updateTitleString];
}

